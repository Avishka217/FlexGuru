<h1>About</h1><br><br>

<b>Our Vision -</b><br>
 Flexible and Easily accessible education for everyone<br>

<b>Who we are</b><br>
We are a complete resource for current or prospective students, Professionals, and Tutors.
We give flexibility to the students and professionals to request or choose and learn the exact content they wish to learn. <br>
Without wasting valuable time by following a complete course or reading an entire book, 
we offer ranking for the students and tutors to improve their rating to get the best education while enjoying benefits from Flexguru. <br>
Customized learning requests are one of the uniqueness of our platform.
Every student will be able to request the tutor while mentioning his needs and preferences in order to succeed easily.<br>

<b>How it all started</b><br>

We found that there is an issue in the common E-education system after the pandemic strikes and students moving towards online learning. <br>
"Without a problem, there is no innovation" in that perspective the problem we figured out is, students and professionals get doubts in specific parts of the subject 
in the learning process but there are no perfect solutions to overcome it. According to our country, every student does not get enough resources while learning and clearing doubts. <br>
so we group of undergraduate students came up with this idea to help students and professionals to give an easily accessible and flexible education as our vision suggests with a personalized learning dashboard.<br>






